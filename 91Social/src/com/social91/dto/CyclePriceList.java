package com.social91.dto;

public class CyclePriceList {
	private int framePrice;
	private int handleBarBrakesPrice;
	private int seatingPrice;
	private int wheelsPrice; 
	private int chainAssemblyPrice;
	private int totalPrice;
	public int getFramePrice() {
		return framePrice;
	}
	public void setFramePrice(int framePrice) {
		this.framePrice = framePrice;
	}
	public int getHandleBarBrakesPrice() {
		return handleBarBrakesPrice;
	}
	public void setHandleBarBrakesPrice(int handleBarBrakesPrice) {
		this.handleBarBrakesPrice = handleBarBrakesPrice;
	}
	public int getSeatingPrice() {
		return seatingPrice;
	}
	public void setSeatingPrice(int seatingPrice) {
		this.seatingPrice = seatingPrice;
	}
	public int getWheelsPrice() {
		return wheelsPrice;
	}
	public void setWheelsPrice(int wheelsPrice) {
		this.wheelsPrice = wheelsPrice;
	}
	public int getChainAssemblyPrice() {
		return chainAssemblyPrice;
	}
	public void setChainAssemblyPrice(int chainAssemblyPrice) {
		this.chainAssemblyPrice = chainAssemblyPrice;
	}
	public int getTotalPrice() {
		return totalPrice;
	}
	public void setTotalPrice(int totalPrice) {
		this.totalPrice = totalPrice;
	}
}
